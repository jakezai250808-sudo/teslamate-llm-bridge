  CASE WHEN cp.geofence_id = 903 THEN 1 ELSE 3 END,
  CASE WHEN cp.geofence_id = 903 THEN 400 ELSE 32 END,
  CASE WHEN cp.geofence_id = 903 THEN 380 ELSE 220 END,
  CASE WHEN cp.geofence_id = 903 THEN TRUE ELSE FALSE END,
  CASE WHEN cp.geofence_id = 903 THEN 'Tesla' ELSE NULL END,
  CASE WHEN cp.geofence_id = 903 THEN 'DC' ELSE NULL END,
  cp.start_ideal_range_km + (cp.end_ideal_range_km - cp.start_ideal_range_km) * i / 3,
  cp.start_rated_range_km + (cp.end_rated_range_km - cp.start_rated_range_km) * i / 3,
  cp.outside_temp_avg,
  cp.start_battery_level + ROUND((cp.end_battery_level - cp.start_battery_level) * i / 3)
FROM public.charging_processes cp,
     generate_series(0, 3) i
WHERE cp.car_id = 99;

